/*use switch case on a string

If the string is + show sum of 2 nums
- , * ., / ,% = show respective results*/
var a=10
var b=12
var c='+'

switch(c)
{
    case "+":console.log(a+b); 
break;
case "-":console.log(a-b);  break;
case "*": console.log(a*b);break;
case "/": console.log(a/b);break;
case "%": console.log(a%b);break;
default :console.log("invalid choice");


}


